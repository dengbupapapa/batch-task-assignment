function expectedException(actual, expected) {
  if (!actual || !expected) {
    return false;
  }

  if (Object.prototype.toString.call(expected) == '[object RegExp]') {
    return expected.test(actual);
  }

  try {
    if (actual instanceof expected) {
      return true;
    }
  } catch (e) {
    // Ignore.  The instanceof check doesn't work for arrow functions.
  }

  if (Error.isPrototypeOf(expected)) {
    return false;
  }

  return expected.call({}, actual) === true;
}

function _tryBlock(block) {
  var error;
  try {
    block();
  } catch (e) {
    error = e;
  }
  return error;
}

function _throws(shouldThrow, block, expected, message) {
  var actual;

  if (typeof block !== 'function') {
    throw new TypeError('"block" argument must be a function');
  }

  if (typeof expected === 'string') {
    message = expected;
    expected = null;
  }

  actual = _tryBlock(block);

  message = (expected && expected.name ? ' (' + expected.name + ').' : '.') +
            (message ? ' ' + message : '.');

  if (shouldThrow && !actual) {
    fail(actual, expected, 'Missing expected exception' + message);
  }

  var userProvidedMessage = typeof message === 'string';
  var isUnwantedException = !shouldThrow && isError(actual);
  var isUnexpectedException = !shouldThrow && actual && !expected;

  if ((isUnwantedException &&
      userProvidedMessage &&
      expectedException(actual, expected)) ||
      isUnexpectedException) {
    fail(actual, expected, 'Got unwanted exception' + message);
  }

  if ((shouldThrow && actual && expected &&
      !expectedException(actual, expected)) || (!shouldThrow && actual)) {
    throw actual;
  }
}

// 11. Expected to throw an error:
// assert.throws(block, Error_opt, message_opt);
assert.throws = throws;
function throws(block, /*optional*/error, /*optional*/message) {
  _throws(true, block, error, message);
}

// EXTENSION! This is annoying to write outside this module.
assert.doesNotThrow = doesNotThrow;
function doesNotThrow(block, /*optional*/error, /*optional*/message) {
  _throws(false, block, error, message);
}

assert.ifError = ifError;
function ifError(err) {
  if (err) throw err;
}

function batchTaskAssignment({
    delay = 200,
    unitMaximum = 10
} = {
    delay: 200,
    unitMaximum: 10
}) {

    assert(typeof delay === 'number', 'delay typeof number');
    assert(delay >= 0, 'delay is gte 0');

    assert(typeof unitMaximum === 'number', 'unitMaximum typeof number');
    assert(unitMaximum >= 0, 'unitMaximum is gte 0');
    assert(Math.floor(unitMaximum) === unitMaximum, 'unitMaximum is integer');

    let timer;
    let working;

    return function dispose(disposeFn) {

        assert(typeof disposeFn === 'function', 'dispose callback instanceof function');

        //任务仓库相关参数
        let resolves = [];
        let datas = [];
        let dataLengths = [];
        let tasks = {
            resolves,
            datas,
            dataLengths
        };

        //触发时机
        function timing() {

            if (timer) clearTimeout(timer);

            if (tasks.datas.length >= unitMaximum) {
                return unitMaximumSpliceTasksAndDisposeFn();
            }

            timer = setTimeout(function () {
                unitMaximumSpliceTasksAndDisposeFn();
            }, delay);
        }

        //按单次处理量限制截取任务仓库数据和防抖回调
        function unitMaximumSpliceTasksAndDisposeFn() {

            let unitDatas = datas.splice(0, unitMaximum);

            working = true;
            disposeFn(unitDatas, resolvesCallback);
        }

        //响应结果相关参数
        //因为不够取而导致遗留到下一阶段的暂存区
        let unitResultTemporaryStorage = [];

        function resolvesCallback(unitResult) {

            assert(Object.prototype.toString.call(unitResult) === '[object Array]', 'unitResult instanceof Array');

            //在前面插入上一次递归遗留的数据
            unitResult.unshift(...unitResultTemporaryStorage.splice(0));

            //按dataLengths正序取result
            while (true) {

                //不够取就break，留给下一个递归。
                if (unitResult.length < tasks.dataLengths[0] || !!!tasks.dataLengths[0]) {
                    break;
                }

                //够取直接取
                let currentItemResult = unitResult.splice(0, tasks.dataLengths[0]);
                tasks.resolves[0](currentItemResult);
                tasks.resolves.shift();
                tasks.dataLengths.shift();
            }

            //剩余部分放暂存区
            unitResultTemporaryStorage.push(...unitResult);

            //如果还有未处理的数据那么递归
            if (tasks.datas.length > 0) {
                timing();
                //否则重置状态
            } else {
                working = false;
            }
            /*
                问题
                可能会存在并行的unitMaximumSpliceTasksAndDisposeFn 导致 unitResult和 resolve、lengths不对应
                 解决办法
                当有超出单次处理量长度时，关闭防抖功能，使用直接触发功能进行递归依次请求并设置标识处理中，后进入的任务也排列到其中，除非在下次任务进入前递归完毕，不然不重置标识。
            */
        }

        return function task(data) {

            assert(Object.prototype.toString.call(data) === '[object Array]', 'task instanceof Array');

            return new Promise(function (resolve) {

                //任务仓库存储
                tasks.resolves.push(resolve);
                tasks.datas.push(...data);
                tasks.dataLengths.push(data.length);

                if (working) return 'working';

                timing();
            });
        };
    };
}

export default batchTaskAssignment;
